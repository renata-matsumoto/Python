lista = [2,3,4,5]

nova_lista = lista[0]
print(nova_lista)

nova_lista1 = lista[0:2] #começa no indice 0 e termina no indice 1. o indice 2 não entra.
print(nova_lista1)

nova_lista2 = lista[0:2:2]#salta de 2 em 2
print(nova_lista2)

lista3 = ['p','y','t','h','o','n']
nova_lista3 = lista3[::-1]
print(nova_lista3)