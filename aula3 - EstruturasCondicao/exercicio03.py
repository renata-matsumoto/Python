""" Prática Proposta - Aula 03 Como sugestão de prática,indicamos você a criar um  novo script python seguindo as orientações a seguir:
1-Crie uma variável número e atribua o valor 15 a ela.
2-Crie um comando de condição para verificar se ela é == a 15,Menor que 15, maior que 15.
3-Informar através de print a informação para o usuário. """

numero = 15

if numero == 15:
    print("O número é igual a 15!")
elif numero < 15:
    print("O numero é menor que 15!")
elif numero > 15:
    print("O número é maior que 15!")

