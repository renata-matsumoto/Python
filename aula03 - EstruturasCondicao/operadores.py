# Operadores Relacionais > < >= <=
# Operadores de igualdade == !=

numero = 100 == 100
print(numero)
print(type(numero))

numero = 100 != 100
print(numero)
print(type(numero))

numero = 100 > 100
print(numero)
print(type(numero))

numero = 100 < 100
print(numero)
print(type(numero))

numero = 100 >= 100
print(numero)
print(type(numero))


numero = 100 <= 100
print(numero)
print(type(numero))

